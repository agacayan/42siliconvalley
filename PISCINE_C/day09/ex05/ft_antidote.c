int		ft_antidote(int i, int j, int k)
{
	if ((i > k && i < j) || (i < k && i > j))
		return (i);
	else if ((k > i && k < j) || (k < i && k > j))
		return (k);
	else if ((j > i && j < k) || (j < i && j > k))
		return (j);
	else
		return (0);
}
